# list_slicing.py

# Let's create a list of numbers from 1 to 10
numbers = list(range(1, 11))

# Extract the first 5 numbers
first_five = numbers[:5]

# Reverse that portion of the list
reversed_five = first_five[::-1]

# Print both results
print("First five numbers:", first_five)
print("Reversed version:", reversed_five)
